const TAGS_BY_CODE = Object.freeze(
    Object.fromEntries(
        Object.entries(TAGS).map(([name, def]) => [
            def.code,
            {
                name,
                type: def.type,
            },
        ]),
    ),
);


function getTagDefinition(tag) {
    if (typeof tag === 'string') {
        if (TAGS[tag]) {
            return {
                name: tag,
                code: TAGS[tag].code,
                type: TAGS[tag].type,
            };
        }
        if (/^0x[0-9a-f]+$/iu.test(tag)) {
            const code = Number.parseInt(tag, 16) >>> 0;
            return {
                name: TAGS_BY_CODE[code] ? TAGS_BY_CODE[code].name : toCodeHex(code),
                code,
                type: TAGS_BY_CODE[code] ? TAGS_BY_CODE[code].type : undefined,
            };
        }
        throw new Error(`Unknown RSCP tag name: ${tag}`);
    }

    if (typeof tag === 'number' && Number.isFinite(tag)) {
        const code = tag >>> 0;
        return {
            name: TAGS_BY_CODE[code] ? TAGS_BY_CODE[code].name : toCodeHex(code),
            code,
            type: TAGS_BY_CODE[code] ? TAGS_BY_CODE[code].type : undefined,
        };
    }

    throw new Error(`Unsupported RSCP tag identifier: ${String(tag)}`);
}

function toCodeHex(code) {
    return `0x${(code >>> 0).toString(16).padStart(8, '0').toUpperCase()}`;
}

